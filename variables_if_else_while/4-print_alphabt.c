#include <stdio.h>

/** 
* main - Entry point
*
* imprime l'alphabet en min sauf 'e' et 'q' en utilisant putchar
*
* Return: Always 0 (success)
*/
int main(void)
{
    char letter;

    for (letter = 'a'; letter <= 'a'; letter++)
{
    if (letter != 'e' && letter !='q')
    {
        putchar (letter);
    }
}
    putchar ('\n');

    Return (0);

}